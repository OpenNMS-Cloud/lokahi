package org.opennms.horizon.kafkahelper.internals;

import com.fasterxml.jackson.annotation.JsonProperty;

public class KafkaRestCreateConsumerRequest {
    private String name;
    private String format = "binary";

    @JsonProperty(value = "auto.offset.reset", defaultValue = "earliest")
    private String autoOffsetReset = "earliest";

    @JsonProperty(value = "auto.commit.enable", defaultValue = "false")
    private String autoCommitEnable = "false";

//========================================
// Getters and Setters
//----------------------------------------

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFormat() {
        return format;
    }

    public void setFormat(String format) {
        this.format = format;
    }

    public String getAutoOffsetReset() {
        return autoOffsetReset;
    }

    public void setAutoOffsetReset(String autoOffsetReset) {
        this.autoOffsetReset = autoOffsetReset;
    }

    public String getAutoCommitEnable() {
        return autoCommitEnable;
    }

    public void setAutoCommitEnable(String autoCommitEnable) {
        this.autoCommitEnable = autoCommitEnable;
    }
}

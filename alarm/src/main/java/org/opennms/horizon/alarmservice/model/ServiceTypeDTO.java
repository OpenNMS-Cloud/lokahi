package org.opennms.horizon.alarmservice.model;

import java.io.Serializable;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@AllArgsConstructor
@NoArgsConstructor
public class ServiceTypeDTO implements Serializable {

    private static final long serialVersionUID = -459218937667452586L;

    private Integer serviceId;
    private String name;


    /** {@inheritDoc} */
    @Override
    public boolean equals(final Object obj) {
        if (obj instanceof ServiceTypeDTO) {
            ServiceTypeDTO t = (ServiceTypeDTO)obj;
            return serviceId.equals(t.getServiceId());
        }
        return false;
    }

    @Override
    public int hashCode() {
        return serviceId.intValue();
    }

}

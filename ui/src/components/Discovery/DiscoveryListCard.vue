        :icon="Icons.Help"
        @click="$emit('showInstructions')"
      />
    </div>
    <div v-if="list.length > 0">
      <div
        v-for="item in list"
        :key="item.id"
        class="discovery-name"
        :class="{ selected: selectedId == item.id }"
      >
        <div
          @click="$emit('selectDiscovery', item)"
          class="name pointer"
        >
          {{ item.name?.toUpperCase() }}
        </div>
        <FeatherTooltip
          :title="`Toggle ${item.name?.toUpperCase()} on/off`"
          v-slot="{ attrs, on }"
        >
          <BasicToggle
            v-bind="attrs"
            v-on="on"
            v-if="passive"
            :toggle="(item as PassiveDiscovery).toggle"
            @toggle="(isToggled) => $emit('toggleDiscovery', item.id, isToggled)"
          />
        </FeatherTooltip>
      </div>
    </div>
    <div
      v-else
      class="empty"
    >
      <FeatherIcon
        :icon="Warning"
        class="icon"
      />You have no {{ passive ? 'passive' : 'active ' }} discovery
